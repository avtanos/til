def _fmt_float(v: float) -> str:
    # Match interpreter formatting: remove trailing .0 when close to an integer.
    if abs(v - round(v)) < 1e-9:
        return str(int(round(v)))
    return str(v)


def _fmt_bool(v: bool) -> str:
    return "чын" if v else "жалган"


def _add_sem_task(
    *,
    group: str,
    title: str,
    title_ky: str,
    difficulty: int,
    code: str,
    example_in: str,
    example_out: str,
    no_split_numeric_input: bool = False,
    hint: str | None = None,
    hint_ky: str | None = None,
) -> None:
    global TASKS
    global _next_sem_id
    TASKS.append(
        {
            "id": _next_sem_id,
            "group": group,
            "title": title,
            "title_ky": title_ky,
            "difficulty": difficulty,
            "statement": f"Семантика: проверь поведение оператора. Код:\n{code}",
            "statement_ky": f"Семантика: проверь поведение оператора. Код:\n{code}",
            "input_format": "Ввод задаётся в поле на странице.",
            "output_format": "Ожидаемый вывод.",
            "example_in": example_in,
            "example_out": example_out,
            "hint": hint
            or f"Подсказка: обрати внимание на оператор из темы «{group}» и попробуй мысленно выполнить код построчно, чтобы предсказать вывод.",
            "hint_ky": hint_ky
            or f"Ишара: «{group}» темасындагы операторду байкап, кодду сап-сап болуп ой жүгүртүп аткарып көр — чыгыш кандай болорун алдын ала айт.",
            **({"no_split_numeric_input": True} if no_split_numeric_input else {}),
        }
    )
    _next_sem_id += 1


